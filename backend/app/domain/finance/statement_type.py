"""财务报表类型代码数据字典 — V12 §10.4.

定义 balance_sheet / income_statement / cash_flow 三表的 statement_type 代码含义。
不使用 MySQL ENUM，保持与 SQLite 兼容。

权威来源: Wind 量化研究数据库 V4.47
  https://index.cslt.org/mediawiki/images/7/79/Wind资讯量化研究数据库V4.47.pdf

完整代码表:
  408001000  合并报表
  408002000  合并报表（单季度）
  408003000  合并报表（单季度调整）
  408004000  合并报表（调整）
  408005000  合并报表（更正前）
  408006000  母公司报表
  408007000  母公司报表（单季度）
  408008000  母公司报表（单季度调整）
  408009000  母公司报表（调整）
  408010000  母公司报表（更正前）

项目数据现状 (V12 §10.4 / DESIGN_V12.md):
  当前三表数据主要为 statement_type=408006000（母公司报表口径）。
  商誉、固定资产、在建工程等合并口径字段覆盖率不足时不进入依赖规则。

推荐分析口径 (DATA_CONTRACT.md):
  statement_type=408001000（合并报表）为主口径。
"""

from enum import StrEnum


class StatementTypeCode(StrEnum):
    """财务报表类型代码 — 与 Wind 数据标准对齐.

    408001000: 合并报表（推荐主分析口径）
    408006000: 母公司报表（当前数据主要口径）
    """

    # 合并报表 — 推荐主分析口径 (DATA_CONTRACT.md)
    CONSOLIDATED = "408001000"

    # 母公司报表 — 当前数据主要口径 (DESIGN_V12.md §10.4)
    PARENT_COMPANY = "408006000"


# ── 完整数据字典 ─────────────────────────────────────────

STATEMENT_TYPE_DICT: dict[str, str] = {
    "408001000": "合并报表",
    "408002000": "合并报表（单季度）",
    "408003000": "合并报表（单季度调整）",
    "408004000": "合并报表（调整）",
    "408005000": "合并报表（更正前）",
    "408006000": "母公司报表",
    "408007000": "母公司报表（单季度）",
    "408008000": "母公司报表（单季度调整）",
    "408009000": "母公司报表（调整）",
    "408010000": "母公司报表（更正前）",
}


def describe_statement_type(code: str) -> str:
    """返回 statement_type 代码的人类可读描述."""
    return STATEMENT_TYPE_DICT.get(code, f"未知代码: {code}")


def is_consolidated(code: str) -> bool:
    """判断是否为合并报表口径 (408001000–408005000)."""
    return code in {"408001000", "408002000", "408003000", "408004000", "408005000"}


def is_parent_company(code: str) -> bool:
    """判断是否为母公司报表口径 (408006000–408010000)."""
    return code in {"408006000", "408007000", "408008000", "408009000", "408010000"}
